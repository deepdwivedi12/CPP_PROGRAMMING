// Question: Write a C++ program to declare and print different data types.

#include <iostream>
using namespace std;

int main()
{
    int age = 18;
    float marks = 89.5;
    char grade = 'A';
    string name = "Deep";

    cout << "Name : " << name << endl;
    cout << "Age : " << age << endl;
    cout << "Marks : " << marks << endl;
    cout << "Grade : " << grade << endl;

    return 0;
}
